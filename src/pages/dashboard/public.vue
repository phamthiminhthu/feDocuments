<template>
  <div class="mb-48">
    <div class="text-center landing-title-page mb-5">Document Public</div>
    <Documents
      :documents="filterDocuments"
      @documents-updated="handleChangeListDocument"
    ></Documents>
  </div>
</template>
<script>
export default {
  name: "Public",
  layout: "base",
  mounted() {
    this.fetchDocumentPublic();
  },
  computed: {
    documents() {
      return this.$store.getters["documents/getDocuments"];
    },
    filterDocuments() {
      if (this.documents && Array.isArray(this.documents)) {
        return this.documents.map((document) => {
          return {
            id: document.documentKey,
            authors: document.authors,
            title: document.title,
            publishingYear: document.publishingYear,
            createdAt: document.createdAt,
            typeDocumentsList: document.typeDocumentsList,
            loved: document.loved,
          };
        });
      }
    },
  },
  methods: {
    async fetchDocumentPublic() {
      await this.$store.dispatch("documents/fetchDocumentsPublic");
    },
    handleChangeListDocument() {
      this.fetchDocumentPublic();
    },
  },
};
</script>
